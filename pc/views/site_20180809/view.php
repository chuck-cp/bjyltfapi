            <p class="title1">2.【介绍业务合作人】的好处：</p>
            <p class="content">
                玉龙传媒将额外拿出一部分佣金奖励介绍业务合作人者，介绍业务合作人者将可轻松赚取长期稳定的额外佣金，其佣金是你所介绍的业务合作人所拿直接佣金的10%，以上是税后净得的佣金，玉龙传媒已经为你承担了相应的税金。</p>
            <p class="content">
                需要说明的是你所介绍的业务合作人也可能再介绍业务合作人，他（她）也可享受介绍业务合作人应拿的额外佣金，他（她）的这部分佣金不计算在你的10%的佣金基数中，即你所拿的10%的额外佣金的计算基数只是你介绍的业务合作人自己直接承揽业务所拿的佣金，玉龙传媒没有设立多级享受业务提成的政策。</p>
