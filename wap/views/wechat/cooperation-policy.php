        <p class="wz-nr">在任何情况下，玉龙传媒及其关联方和代理均不对因超出其合理控制的自然灾害、外力或原因（包括但不限于自然灾害、网络故障、计算机、电信或任何其他设备故障、电力故障、罢工、劳资纠纷、骚乱、叛乱、内乱、劳动力或物资短缺、恐怖主义、战争、政府行动、国内外法院或法庭的命令）直接或间接导致的产品和服务的任何延迟、故障或中断承担责任。</p>
