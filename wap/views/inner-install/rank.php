            loadData(1,'month_shop_number','month_screen_number');
            function loadData(tm,field,field2) {
                $.ajax({
                    type : "GET", //提交方式
                    url : baseApiUrl+"/performance-rank/index/"+tm+"&token=<?=$token?>",//路径
                    data : {},
                    success:function (phpdata) {
                        var shtml = '';
                        if(phpdata.data !== null){
                            $.each(phpdata.data,function (index,value) {
                                //头像处理
                                var tx = '';
                                if(value.tx !== ''){
                                    tx = value.tx+'?imageView2/0/w/45';
                                }else {
                                    tx = '/static/images/head_pic.png';
                                }
                                //排名
                                var pm = '';
                                if(value[field] > 0){
                                    if(index == 0){
                                        pm = '<img class="paimin" src="/static/images/gold.png" >';
                                    }else if(index == 1){
                                        pm = '<img class="paimin" src="/static/images/silver.png" >';
                                    }else if(index == 2){
                                        pm = '<img class="paimin" src="/static/images/copper.png">';
                                    }else {
                                        pm = '<span class="paimin_no">'+(parseInt(index)+1)+'</span>';
                                    }
                                }else{
                                    pm = '<span class="paimin_no"></span>';
                                }
                                shtml += '<li><div class="paimin_box">'+pm+'</div><div class="touxiang_box"><img class="touxiang" src="'+tx+'" ></div><div class="info_box"><p class="info_name">'+value.member_name+'</p><p><span class="daian">待安装店铺 <span>'+value.wait_install_shop_number+'</span></span>&nbsp;&nbsp;&nbsp;&nbsp;<span class="yian">已安装屏幕 <span>'+value[field2]+'</span></span></p></div><div class="achievement"><p>已安装店铺</p><p class="shop_tt" ><span class="shop_no">'+value[field]+'</span></p></div></li>';
                                if(value.member_id == member_id){
                                    if(value.tx !== ''){
                                        $('.achievement_cur .touxiang').attr('src',value.tx+'?imageView2/0/w/45');
                                    }else {
                                        $('.achievement_cur .touxiang').attr('src','/static/images/head_pic.png');
                                    }
                                    $('.achievement_cur .info_name').html(value.member_name);
                                    $('.achievement_cur .daian span').html(value.wait_install_shop_number);
                                    $('.achievement_cur .shop_no').html(value[field]);
                                    $('.achievement_cur .yian span').html(value[field2]);
                                    if(parseInt(value[field]) > 0){
                                        $('.achievement_cur ol .paimin_no span').html(parseInt(index)+1);
                                    }else {
                                        $('.achievement_cur ol .paimin_no span').html('');
                                    }


                                }
                            })
                            //当前用户名次显示

                        }
                        $('.achievement_cur ul').html(shtml);						
                    }
                });
            }
            $('.swiper-slide').live('click',function () {
                var cur_index = $(this).index();
                var field = '';
                switch (cur_index){
                    case 0:
                        field = 'month_shop_number';
                        break;
                    case 1:
                        field = 'last_month_shop_number';
                        break;
                    case 2:
                        field = 'last_half_past_month_shop_number';
                        break;
                    case 3:
                        field = 'last_week_shop_number';
                        break;
                    case 4:
                        field = 'count_shop_number';
                        break;
                }
                var field2 = field.replace('shop','screen');
                loadData(parseInt(cur_index)+1,field,field2)
            })
