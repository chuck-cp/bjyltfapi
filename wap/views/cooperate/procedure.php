<div class="yx_cjwt">
    <h3 class="yx_biaoti">常见问题</h3>
    <ol>
        <li>1、广告从购买到投放最短周期为多少天？</br>
            最短周期为15天，因此您只能购买距当天15天之后的广告</li>
        <li>2、所选择的地区广告排期已满怎么办？</br>
            你可以更换投放时间或广告时长、频次来进行重新查询</li>
        <li>3、业务合作人必须填写吗？</br>
            是的，业务合作人可以为您来解答在购买广告中的一些疑惑，为您提供更好的服务。目前我们要求购买广告时必须填写业务合作人信息。</li>
        <li>4、没有业务合作人手机号怎么办？</br>
            您可以致电我们公司客服，我们来为您匹配您所在地区业务合作人</li>
        <li>5、如何查看广告位置及类型？</br>
            您可以在广告查询界面，点击广告位预览。就可以查看可购买的广告位位置、广告类型、频次、时长等信息</li>
        <li>6、什么是定金支付？</br>
            定金支付是指您可以支付总价格百分之三十的金额提前锁定广告排期。尾款需要您在广告播放前七天完成支付，以保证您的广告按期投放。</li>
        <li>7、支付成功之后，提示所选择时间无排期？</br>
            当支付成功后提示时间无排期之后，我们会为你把未播放的日期在播放完成之后为您补播，如果您不同意我们的补播政策，请及时联系我们的客服协商解决。</li>
        <li>8、选择线上支付或线下支付需要多久完成支付？</br>
            如果您选择线上支付，需在30分钟内完成支付，逾期订单将自动关闭，需要您重新查询下单。如果您选择线下支付，需在48小时内完成支付，逾期订单将自动关闭，需要您重新查询下单。我们建议无论您选择线上支付或线下支付，在付款	之前都要再次查询订单，以免出现无排期的情况。</li>
        <li>9、广告购买完成之后怎么提交广告素材？</br>
            您在支付成功之后，需登录<span class="yx_lan">http://www.bjyltf.com</span>点击上传素材，输入您的账号密码，即可以上传广告素材和相关知识产权。您需要在广告投放的前七天完成素材的上传。</li>
        <li>10、尾款到时间未支付需要怎么办？</br>
            由于您个人原因在规定的时间未能完成尾款补交，我们会为您提供三次修改投放时间的权利，您选择时间需要在我们可投放广告的时间内。</li>
    </ol>
